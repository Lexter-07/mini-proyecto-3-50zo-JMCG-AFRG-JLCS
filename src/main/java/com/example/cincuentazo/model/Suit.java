package com.example.cincuentazo.model;
/**
 * Represents the four standard suits of a deck.
 * @author Andrés Felipe Rodríguez García
 * @version 1.0
 */
public enum Suit {

    /** Clubs suit (♣). */
    CLUBS,
    /** Diamonds suit (♦). */
    DIAMONDS,
    /** Hearts suit (♥). */
    HEARTS,
    /** Spades suit (♠). */
    SPADES
}
